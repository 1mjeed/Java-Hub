    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int selectedRow = tblMembers.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "الرجاء اختيار عضو من الجدول للحذف");
            return;
        }

        //--
        int confirm = JOptionPane.showConfirmDialog(this, "هل أنت متأكد من الحذف؟");
        if (confirm == JOptionPane.YES_OPTION) {
            // نأخذ الـ ID من العمود رقم 0
            int id = (int) tblMembers.getValueAt(selectedRow, 0);

            MemberDAO dao = new MemberDAO();
            if (dao.deleteMember(id)) {
                JOptionPane.showMessageDialog(this, "تم الحذف بنجاح");
                loadTable();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void updateMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMemberActionPerformed
        MemberDAO edao = new MemberDAO();
        int selectedRow = tblMembers.getSelectedRow();
        int id = (int) tblMembers.getValueAt(selectedRow, 0);;
        if (edao.editMember(txtName.getText(), txtPhone.getText(), txtEmail.getText(), txtDiseases.getText(), id)) {
            JOptionPane.showMessageDialog(this, "تم تعديل البيانات بنجاح");

            clearFields();
            loadTable();
        } else {
            JOptionPane.showMessageDialog(null, "لم يتم العثور على العضو لتعديله");
        }

    }//GEN-LAST:event_updateMemberActionPerformed
