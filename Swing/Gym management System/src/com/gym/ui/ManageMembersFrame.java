    private void loadTable() {
        String[] columns = {"ID", "Name", "Phone", "Personal ID", "Type", "Join Date", "Email", "Disease",};
        DefaultTableModel model = new DefaultTableModel(columns, 0);

        MemberDAO dao = new MemberDAO();
        List< Member> members = dao.getAllMembers();

        for (Member m : members) {
            Object[] row = {
                m.getId(),
                m.getFullName(),
                m.getPhone(),
                m.getPersonalId(),
                m.getMembershipType(),
                m.getJoinDate(),
                m.getEmail(),
                m.getDiseases()

            };
            model.addRow(row);
        }
        tblMembers.setModel(model);
    }
